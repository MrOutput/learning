SECTION .DATA
NO_ARGS: DB "no args.", 10

SECTION .TEXT
GLOBAL _start

_start:		MOV	RCX,	QWORD [RSP]	;ARGC
		CMP	RCX,	2
		JL	EXIT_NO_ARGS

		XOR	RDI,	RDI

EXIT:		MOV	RAX,	60
		SYSCALL

EXIT_NO_ARGS:	MOV	RSI,	NO_ARGS
		MOV	RDX,	9
		CALL PRINT

		MOV	RDI,	1
		JMP	EXIT

;RSI,	BUF
;RDI,	BUFSIZE

PRINT:		MOV	RAX,	1
		MOV	RDI,	1
		SYSCALL
		RET
